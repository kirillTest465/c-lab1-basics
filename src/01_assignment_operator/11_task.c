int main()
{
    // Объявляем и инициализируем перменные
    double base_a = 6.5; // Длина первого основания трапеции
    double base_b = 4.2; // Длина второго основания трапеции
    double height = 3.8; // Высота трапеции
    double area;         // Переменная для площади трапеции

    // Расчет площади трапеции
    area = 0.5 * (base_a + base_b) * height;
}